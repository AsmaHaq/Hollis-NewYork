//else statement execute when the Boolean expression is false.
public class IfElse {
    public static void main (String [] args) {
        int x= 10;
        //if x value is less than 20 than print the value, or else , print-sorry cannot print.
        if(x > 20) {
            System.out.println(x);
        }
        else//x value is more than 20 then print the value
            System.out.println("sorry cannot print");

    }
}

